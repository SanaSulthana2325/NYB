//switch statemet: used when there are multiple possible values for a single variable

let day = 3
switch(day){
    case 1:
        console.log("monday")
        break;
        case 2:
            console.log("tuesday")
            break;
            case 3:
                console.log("wednesday")
                break;
                default:
                    console.log("Invalid day")

        
        
}

