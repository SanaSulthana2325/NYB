// ternary operator
// Ternary Operator: it is shortcut method for if-else statement injs
// condition ?true : false

// ex
let age = 20
let results = (age >=18) ? "Eligible to vote" : "not Eligible"
console.log(results)

//
let age1 = 10
let result = (age1 >=18) ? "Eligible to vote" : "not Eligible"
console.log(result)

