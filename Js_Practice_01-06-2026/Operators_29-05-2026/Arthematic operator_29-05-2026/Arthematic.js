// decrement: decrement opeartors is used to decrease the value of a variable by 1

let G = 30
G--
console.log(G)

//a.pre-decrement(--a):first decrease the value then use it

let u = 77
console.log(--u)

//b.post-decrement(a--): first use the value then decrease it

let r = 30
console.log(r--)
console.log(r)

